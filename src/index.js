var tasks = require('./tasks');

//= = = = = = = = = = = = = = = = = = = = = = = = = = = = = = = = = = =
//
// - EXPORTS
//
//= = = = = = = = = = = = = = = = = = = = = = = = = = = = = = = = = = =

module.exports = {
  use: function (gulp, lygt) {
    var t = tasks.use(gulp);
    if (lygt) {
      lygt.merge(lygt.tasks, t);
    }
    return {
      tasks: t
    };
  }
};
